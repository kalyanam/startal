CREATE TABLE IF NOT EXISTS PROFILES (
    PROFILE_ID SERIAL PRIMARY KEY,
    FULL_NAME VARCHAR(255) NOT NULL,
    EMAIL VARCHAR(255) NOT NULL,
    CONTACT_NUMBER VARCHAR(255) NOT NULL,
    LOCATION VARCHAR(255) NOT NULL,
    INTERESTED_AREA VARCHAR(255) NOT NULL,
    TOP_PREFERRED_ROLE VARCHAR(255) NOT NULL,
    EXPERIENCE_TYPE VARCHAR(255) NOT NULL,
    EDUCATION_TYPE VARCHAR(255) NOT NULL,
    WILLING_TO_RELOCATE VARCHAR(32) NOT NULL,
    SPONSORSHIP_REQUIRED VARCHAR(32) NOT NULL,
    PREFERRED_COMPANY_SIZE VARCHAR(32) NOT NULL,
    ACTIVE_PASSIVE VARCHAR(12) NOT NULL,
    PREFERRED_EMPLOYMENT_TYPE VARCHAR(32) NOT NULL,
    OK_FOR_VETTING VARCHAR(12) NOT NULL,
    DATE_FOR_VETTING VARCHAR(40) NOT NULL,
    SUBJECT TEXT NOT NULL,
    MESSAGE TEXT NOT NULL,
    JOINED_DATE TIMESTAMP NOT NULL
);